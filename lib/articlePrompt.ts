    "실전 디테일: 직접 써본 듯한 구체성(번거로운 점, 흔한 실수, 상황별 체감)을 자연스럽게 녹인다. 단, 없는 1인칭 경험·가짜 수치·가짜 후기는 만들지 않는다. 관점/각도가 주어지면 그것을 1인칭 근거로 적극 활용한다.",
