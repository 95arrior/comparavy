function humanizeCategory(category: ToolCategory): string {
  return category.replace(/-/g, " ");
}

function buildTool(spec: ExtraToolSpec): AiTool {
  const categoryLabel = humanizeCategory(spec.category);
  const personas = CATEGORY_PERSONAS[spec.category];
  const primaryTags = CATEGORY_TAGS[spec.category];

  return {
    id: spec.slug,
    slug: spec.slug,
    name: spec.name,
    category: spec.category,
    officialUrl: spec.officialUrl,
    iconDomain: spec.iconDomain,
    brandColor: spec.brandColor,
    description: `An AI ${categoryLabel} tool for ${spec.focus}.`,
    bestFor: [
      `${spec.name} fits workflows that need ${spec.focus}.`,
      `Useful when you want a focused ${categoryLabel} workflow.`,
      `A practical choice for comparing ${categoryLabel} tools.`,
    ],
    notFor: [
      `Not the best choice when your main job is outside ${categoryLabel}.`,
      "Not ideal when you need a single tool to do every job.",
    ],
    avoidIf: [
      "You need publish-ready output without review.",
      "Your workflow depends on another category of tool.",
    ],
    useCases: [
      spec.focus,
      `${categoryLabel} workflows`,
      `reviewable AI output`,
    ],
    personas,
    budgetLevel: spec.budgetLevel,
    freePlan: spec.freePlan,
    pricingNote: PRICING_NOTICE,
    pricingLastChecked: PRICING_LAST_CHECKED,
    easeScore: spec.easeScore,
    speedScore: spec.speedScore,
    qualityScore: spec.qualityScore,
    beginnerScore: spec.beginnerScore,
    setupDifficulty: spec.setupDifficulty,
    primaryTags,
    alternatives: spec.alternatives,
  };
}

