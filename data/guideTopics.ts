function howToTopic(input: HowToTopicInput): GuideTopic {
  return topic({
    ...input,
    type: "how-to",
    guideType: "how-to",
    deviceIntent: "both",
    serpDifficulty: "medium",
    serpCompetitors: ["Google how-to results", "AI workflow blogs", "tool list pages"],
    status: "active",
    freshness: "evergreen",
  });
}

